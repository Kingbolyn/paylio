export function getTransactionsByDate(transactions: Transaction[]) {
  const groups: Record<string, Transaction[]> = {}

  for (const txn of transactions) {
    const entry = groups[txn.date] ?? []
    entry.push(txn)
    groups[txn.date] = entry
  }

  return Object.entries(groups).sort(([a], [b]) => new Date(b).getTime() - new Date(a).getTime())
}

export function getRecentTransactions(count = 5): Transaction[] {
  return mockTransactions.slice(0, count)
}
